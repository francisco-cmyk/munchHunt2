export type Option = {
  label: string;
  value: string;
};

type DropDownProps = {
  title: string;
  options: Option[];
  value: string;
  onChange: (params: { name: string; value: string }) => void;
};

export default function DropDown(props: DropDownProps): JSX.Element {
  function handleOnChange(option: Option) {
    props.onChange({ name: props.title.toLowerCase(), value: option.value });
  }

  const menu = props.value.length > 0 ? props.value : props.title;

  return (
    <DropdownMenu>
      <DropdownMenuTrigger
        className='w-full h-[40px] flex justify-between items-center border-2 text-sm
          hover:bg-slate-900 hover:text-white rounded-md m-1 py-2 px-1 focus:outline-orange-300'
      >
        {menu}
        <ChevronDown className='h-4 w-4' />
      </DropdownMenuTrigger>
      <DropdownMenuContent side='bottom'>
        {props.options.map((option, index) => (
          <DropdownMenuItem
            className={`md:w-[170px] bg-slate-900  ${
              props.value === option.value
                ? `bg-customOrange dark:bg-slate-600 text-white`
                : `text-black bg-slate-50 dark:bg-slate-800 dark:text-slate-200 font-roboto`
            }`}
            key={`${index}-${option.value}`}
            onChange={() => handleOnChange(option)}
            onClick={() => handleOnChange(option)}
          >
            {option.label}
          </DropdownMenuItem>
        ))}
      </DropdownMenuContent>
    </DropdownMenu>
  );
}

